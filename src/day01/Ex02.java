package day01;

public class Ex02 {
	public static void main(String[] args) {
		System.out.print("100\n200\n300\n");
//		escape문자를 사용할 경우 ""안에 작성해야 한다.
		
		System.out.println("1\t1234567\t12345678\t1");
//		\t는 spacd와 같이 8칸확보안에 냉용을 채운후 공백을 만든다.
		System.out.println("===============");
		System.out.println("홍길동\t20살");
		System.out.println("김개똥\t30살"	);
		System.out.println("---------------");
		
		System.out.println(" \"  \\");
	}

}

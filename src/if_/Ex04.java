package if_;

public class Ex04 {
	public static void main(String[] args) {
		//if - else
		//참이면 if 거짓이면 else안에 있는 종속문장이 출력된다.(둘중 하나만 실행)
		int num = 10;
		if(num == 10) {
			System.out.println("num은 10과 같다");
		}else {
			System.out.println("num은 10ㅇ 아니다");
		}
		System.out.println("다음 문장들 실행!");
	}
}

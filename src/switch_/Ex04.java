package switch_;

import java.util.Scanner;

public class Ex04 {
	public static void main(String[] args) {
//		반복문
		Scanner input = new Scanner(System.in);
		String name;
		while(true) {
//			참인 식을 넣고 실행해야 한다. (true는 참)
			System.out.println("이름 입력");
			name = input.next();
			System.out.println("입력한 이름 : "+ name);
//		틀을 만들고 안의 식을 작성해 나간다.
		}
	}
}
